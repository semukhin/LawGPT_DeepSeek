                    logging.info(f"[DeepResearch #{self.usage_counter}] –£–¥–∞–ª—è–µ–º –ø–æ—Å–ª–µ–¥–Ω–µ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ user –¥–ª—è –æ–±–µ—Å–ø–µ—á–µ–Ω–∏—è —á–µ—Ä–µ–¥–æ–≤–∞–Ω–∏—è")
                    interleaved_messages = interleaved_messages[:-1]
                
