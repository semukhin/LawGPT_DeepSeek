                            # # –í–∞–ª–∏–¥–∞—Ü–∏—è –Ω–æ–º–µ—Ä–æ–≤ —Å—É–¥–µ–±–Ω—ã—Ö –¥–µ–ª –≤ –æ—Ç–≤–µ—Ç–µ
                            # analysis = validate_court_numbers(analysis, query)
