                es_results = search_results.get("es_results", [])
                web_results = search_results.get("web_results", [])
                
                # –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã –∏–∑ ElasticSearch
