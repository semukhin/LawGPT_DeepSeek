            logging.info(f"[DeepResearch #{self.usage_counter}] –û—Ç–ø—Ä–∞–≤–∫–∞ {len(messages)} —Å–æ–æ–±—â–µ–Ω–∏–π –≤ API, –≤–∫–ª—é—á–∞—è —Å–∏—Å—Ç–µ–º–Ω–æ–µ")
            
