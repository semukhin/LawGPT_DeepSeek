            # –û–ø—Ä–µ–¥–µ–ª—è–µ–º —Ç–∏–ø –∑–∞–ø—Ä–æ—Å–∞, –µ—Å–ª–∏ –Ω–µ —É–∫–∞–∑–∞–Ω
            if is_general is None:
                is_general = self.is_general_query(query)
            
            if is_general:
                # –î–ª—è –æ–±—â–∏—Ö –∑–∞–ø—Ä–æ—Å–æ–≤ –∏—Å–ø–æ–ª—å–∑—É–µ–º –æ–±—ã—á–Ω—ã–π –ø—Ä–æ–º–ø—Ç
                system_prompt = GENERAL_SYSTEM_PROMPT
                messages = [{"role": "system", "content": system_prompt},
                           {"role": "user", "content": query}]
                
                response = await self.deepseek_service.chat_completion(messages)
                return ResearchResult(
                    query=query,
                    analysis=response['choices'][0]['message']['content'],
                    timestamp=self._get_timestamp()
                )
            
            # –î–ª—è —é—Ä–∏–¥–∏—á–µ—Å–∫–∏—Ö –∑–∞–ø—Ä–æ—Å–æ–≤ –≤—ã–ø–æ–ª–Ω—è–µ–º –ø–æ–∏—Å–∫
            search_results = await run_parallel_search(query)
            
            if not search_results or search_results.get("error"):
                logging.warning(f"–ù–µ —É–¥–∞–ª–æ—Å—å –Ω–∞–π—Ç–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã –ø–æ–∏—Å–∫–∞ –¥–ª—è –∑–∞–ø—Ä–æ—Å–∞: {query}")
